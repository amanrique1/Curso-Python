def sumar(op1, op2):
	print("La suma da",op1+op2)

def restar(op1, op2):
	print("La resta da",op1-op2)

def multiplicar(op1, op2):
	print("La multiplicacion da",op1*op2)

def dividir(dividendo, divisor):
	print("La division da",dividendo/divisor)

def potencia(base, exponente):
	print("La potencia da",base**exponente)

def redondear(op1):
	print("El redondeo da",round(op1))